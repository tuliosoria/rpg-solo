import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDir, '..');
const generatedPath = path.join(repoRoot, 'electron', 'steam-app-id.generated.js');
const requireAppId = process.argv.includes('--require') || process.env.REQUIRE_STEAM_APP_ID === '1';

function normalizeSteamAppId(value) {
  const appId = value?.trim();
  if (!appId) return null;

  if (!/^\d+$/.test(appId) || Number.parseInt(appId, 10) <= 0) {
    throw new Error('STEAM_APP_ID must be a positive integer');
  }

  return appId;
}

try {
  const steamAppId = normalizeSteamAppId(process.env.STEAM_APP_ID);

  if (!steamAppId) {
    if (fs.existsSync(generatedPath)) {
      fs.unlinkSync(generatedPath);
    }

    if (requireAppId) {
      throw new Error('STEAM_APP_ID is required for Steam release packaging');
    }

    console.log('No STEAM_APP_ID configured; packaging will run without Steam integration.');
    process.exit(0);
  }

  fs.writeFileSync(
    generatedPath,
    [
      '// Generated by scripts/prepare-steam-app-id.mjs. Do not edit.',
      `module.exports = ${JSON.stringify(steamAppId)};`,
      '',
    ].join('\n'),
    'utf8'
  );

  console.log('Prepared Steam App ID for Electron packaging.');
} catch (error) {
  console.error(error.message);
  process.exit(1);
}
